"""This program takes two input from the user and display the
addition
subtraction
multiplication
division
of the numbers"""

#Taking two input from the user
num1 = float(input('Enter the value of num1: '))
num2 = float(input('Enter the value of num2: '))

addition = num1 + num2  #addition of the numbers
subtraction = num1 - num2  #subtraction of the numbers
multiplication = num1 * num2  #multiplication of the numbers
division = num1 / num2  #division of the numbers

#printing the results
print('The sum is', addition)
print('The subtraction is', subtraction)
print('The multiplication is', multiplication)
print('The division is', division)
